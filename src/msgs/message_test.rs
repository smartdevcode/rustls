macro_rules! test_parse_message(
    ($fn:ident, $file:expr) => {
        #[test]
        fn $fn() {
            let bytes = include_bytes!($file);
            let mut rd = Reader::init(bytes);
            let msg = Message::read(&mut rd)
                .unwrap();
            println!("{:?}", msg);
            assert_eq!(bytes.to_vec(), msg.get_encoding());
        }
    }
);

